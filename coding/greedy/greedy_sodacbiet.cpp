#include <bits/stdc++.h>
using namespace std;
#define filename "sodacbiet"
#define ll                  long long
int main(){
    ll n ; cin >> n;
    string res = "";
    if (n%3 != 0 || n < 6) cout << -1;
    else{
        while (n%9 !=0){
            res += '6';
            n-=6;
        }
        while (n>=9){
            res += '9';
            n-=9;
        }
    }
    cout << res;
    return 0;
}


