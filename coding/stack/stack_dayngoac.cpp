bool check(string s){
    stack<char> st;
    for (char& i : s){
        char p = '\0';
        if (!st.empty()) p = st.top();
        if (i == ')')
        {
            if ( p != '(' ) return false;
            else st.pop();
        }
        else if (i == ']'){
            if ( p != '[' ) return false;
            else st.pop();
        }
        else if (i == '}'){
            if ( p != '{' ) return false;
            else st.pop();
        }
        else st.push(i);
    }
    return st.empty();
}

int main(){
    IO();
    ll t ; cin >> t;
    while (t--){
        string s; cin >> s;
        cout << (check(s)? "YES" : "NO") << "\n";
    }
    return 0;
}


